package abd.p1.model;

public class Amigo {
	private String correo_propio;
	private String correo_amigo;
	public Amigo (){
		
	}
	public String getCorreo_propio() {
		return correo_propio;
	}
	public void setCorreo_propio(String correo_propio) {
		this.correo_propio = correo_propio;
	}
	public String getCorreo_amigo() {
		return correo_amigo;
	}
	public void setCorreo_amigo(String correo_amigo) {
		this.correo_amigo = correo_amigo;
	}
	
	
}
